const panelBackdrop = document.getElementById('panelBackdrop');
const printPlanBtn = document.getElementById('printPlanBtn');

// 페이지 로드 시 실행
document.addEventListener('DOMContentLoaded', () => {
  renderCards();
  updateProgress();
  setupEventListeners();
});

// 카드 렌더링
function renderCards() {
  cardsGrid.innerHTML = '';
  
  const filteredData = currentFilter === 'all' 
    ? weeksData 
    : weeksData.filter(item => item.modalities.includes(currentFilter));

  if (filteredData.length === 0) {
    cardsGrid.innerHTML = `<p class="no-results">필터에 해당하는 주차가 없습니다.</p>`;
    return;
  }

  filteredData.forEach(item => {
    const isChecked = checkedWeeks.includes(item.week);
    const card = document.createElement('div');
    card.className = `card ${isChecked ? 'completed' : ''}`;
    card.setAttribute('data-week', item.week);

    const badges = item.modalities.map(mod => {
      let label = '';
      switch (mod) {
        case 'visual': label = '🎨 미술'; break;
        case 'movement': label = '🩰 동작'; break;
        case 'music': label = '🎵 음악'; break;
        case 'drama': label = '🎭 연극/놀이'; break;
        case 'writing': label = '✍️ 글쓰기'; break;
      }
      return `<span class="badge ${mod}">${label}</span>`;
    }).join(' ');

    card.innerHTML = `
      <div class="card-header">
        <span class="week-num">${item.week}주차</span>
        <input type="checkbox" class="card-checkbox" data-week="${item.week}" ${isChecked ? 'checked' : ''}>
      </div>
      <h3>${item.title}</h3>
      <div class="badges-container">${badges}</div>
      <p class="card-summary">${item.summary}</p>
      <button class="detail-btn" type="button">자세히 보기</button>
    `;

    card.addEventListener('click', () => openDetails(item.week));

    // 체크박스 클릭 핸들러 등록
    const checkbox = card.querySelector('.card-checkbox');
    checkbox.addEventListener('click', (e) => e.stopPropagation());
    checkbox.addEventListener('change', (e) => {
      e.stopPropagation();
      toggleWeekProgress(item.week, checkbox.checked);
    });

    const detailBtn = card.querySelector('.detail-btn');
    detailBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      openDetails(item.week);
    });

    cardsGrid.appendChild(card);
  });
}

// 상세 정보 패널 열기
window.openDetails = function(weekNum) {
  const item = weeksData.find(d => d.week === weekNum);
  if (!item) return;

  const badges = item.modalities.map(mod => {
    let label = '';
    switch (mod) {
      case 'visual': label = '🎨 미술치료'; break;
      case 'movement': label = '🩰 동작치료'; break;
      case 'music': label = '🎵 음악치료'; break;
      case 'drama': label = '🎭 연극/놀이치료'; break;
      case 'writing': label = '✍️ 시/글쓰기치료'; break;
    }
    return `<span class="badge ${mod}">${label}</span>`;
  }).join(' ');

  const materials = item.materials.map(mat => `<li>${mat}</li>`).join('');

  const stepsHTML = item.steps.map((step, idx) => `
    <div class="step-card">
      <div class="step-header">
        <span class="step-num">단계 ${idx + 1}</span>
        <span class="step-time">⏱️ ${step.time}</span>
      </div>
      <div class="step-name">${step.name}</div>
      <p class="step-desc">${step.desc}</p>
    </div>
  `).join('');

  panelBody.innerHTML = `
    <div class="panel-week">${item.week}주차 프로그램 가이드</div>
    <h2 class="panel-title">${item.title}</h2>
    
    <div class="panel-meta">
      <div class="meta-item">
        <strong>주요 매체:</strong> ${badges}
      </div>
    </div>

    <div class="section-title">학습 목표 (Objectives)</div>
    <ul style="padding-left: 1.25rem; font-size: 0.9rem; color: var(--text-secondary); line-height: 1.7;">
      ${item.objectives.map(obj => `<li style="margin-bottom: 0.5rem;">${obj}</li>`).join('')}
    </ul>

    <div class="section-title">학술적 이론 배경 (Theoretical Foundations)</div>
    <div class="theory-text">${item.theory}</div>

    <div class="section-title">학술적 연구 근거 (Scientific Evidence)</div>
    <div class="theory-text" style="border-color: rgba(16, 185, 129, 0.3); background-color: rgba(16, 185, 129, 0.02); color: #a7f3d0; font-size: 0.85rem; line-height: 1.6;">
      🔬 <strong>국내외 KCI 등재지 및 SCI급 학술지 연구 근거:</strong><br><br>${item.evidence}
    </div>

    <div class="section-title">필요 준비물 (Materials)</div>
    <ul class="materials-list">${materials}</ul>

    <div class="section-title">단계별 워크숍 진행 프로세스 (Workshop Process)</div>
    <div class="process-timeline">${stepsHTML}</div>

    <div class="section-title">자기 성찰 및 과제 (Reflective Journal)</div>
    <div class="theory-text" style="border-color: rgba(99, 102, 241, 0.3); background-color: rgba(99, 102, 241, 0.02); font-weight: 500; color: #a5b4fc;">
      ❓ ${item.reflection}
    </div>
  `;

  detailsPanel.classList.add('open');
  document.body.style.overflow = 'hidden'; // 뒷배경 스크롤 방지
};

// 상세 정보 패널 닫기
function closeDetails() {
  detailsPanel.classList.remove('open');
  document.body.style.overflow = '';
}

// 저장된 진행률을 안전하게 불러오기
function loadProgress() {
  try {
    const savedWeeks = JSON.parse(localStorage.getItem(STORAGE_KEY)) || [];
    const validWeeks = new Set(weeksData.map(item => item.week));
    return [...new Set(savedWeeks)]
      .filter(week => validWeeks.has(week))
      .sort((a, b) => a - b);
  } catch (error) {
    localStorage.removeItem(STORAGE_KEY);
    return [];
  }
}

// 진행 상황 업데이트
function toggleWeekProgress(weekNum, isCompleted) {
  if (isCompleted) {
    if (!checkedWeeks.includes(weekNum)) {
      checkedWeeks.push(weekNum);
    }
  } else {
    checkedWeeks = checkedWeeks.filter(w => w !== weekNum);
  }

  checkedWeeks.sort((a, b) => a - b);
  
  localStorage.setItem(STORAGE_KEY, JSON.stringify(checkedWeeks));
  updateProgress();
  
  // 현재 카드의 상태 클래스 업데이트
  const card = document.querySelector(`.card[data-week="${weekNum}"]`);
  if (card) {
    if (isCompleted) card.classList.add('completed');
    else card.classList.remove('completed');
  }
}

// 진행바 및 텍스트 상태 동기화
function updateProgress() {
  const totalWeeks = weeksData.length;
  const completedCount = checkedWeeks.length;
  const percent = Math.round((completedCount / totalWeeks) * 100);
  
  progressBar.style.width = `${percent}%`;
  progressPercent.textContent = `${percent}% 완료`;
  progressCount.textContent = `${completedCount} / ${totalWeeks} 주차`;
}

// 이벤트 리스너 바인딩
function setupEventListeners() {
  // 필터 버튼 클릭
  filterButtons.forEach(btn => {
    btn.addEventListener('click', () => {
      filterButtons.forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
      currentFilter = btn.getAttribute('data-modality');
      renderCards();
    });
  });

  // 패널 닫기 버튼
  closePanelBtn.addEventListener('click', closeDetails);
  panelBackdrop.addEventListener('click', closeDetails);
  printPlanBtn.addEventListener('click', () => window.print());

  // ESC 키 클릭시 패널 닫기
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && detailsPanel.classList.contains('open')) {
      closeDetails();
    }
  });
}
